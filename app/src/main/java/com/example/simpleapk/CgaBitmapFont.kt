    fun drawChar(canvas: Canvas, ch: Char, x: Float, y: Float, scaleX: Float, scaleY: Float, paint: Paint) {
        val key = if (ch.code in 97..122) ch.uppercaseChar() else ch
        val glyph = glyphs[key] ?: glyphs['?']!!
        for (row in 0 until 8) {
            val bits = glyph[row]
            for (col in 0 until 8) {
                if ((bits and (0x80 ushr col)) != 0) {
                    canvas.drawRect(
                        x + col * scaleX,
                        y + row * scaleY,
                        x + (col + 1) * scaleX,
                        y + (row + 1) * scaleY,
                        paint
                    )
                }
            }
        }
    }
